export default function AdminSidebar() {
  const pathname = usePathname();
  const { user, isAdmin } = useAuth();

  // Filtrar items según rol
  const allowedItems = menuItems.filter(item => {
    // Dashboard y Órdenes de compra: todos pueden ver
    if (item.href === '/dashboard' || item.href === '/dashboard/ordenes-compra') {
      return true;
    }
    // Reportes: admin y supervisor
    if (item.href === '/dashboard/reportes') {
      return isAdmin || user?.role === 'supervisor';
    }
    // Todo lo demás: solo admin
    return isAdmin;
  });

  return (
    <aside className="w-64 bg-white border-r border-gray-200 min-h-screen p-4">
      <nav className="space-y-1">
        {allowedItems.map((item) => {
          const isActive = pathname === item.href;
          return (
            <Link
              key={item.name}
              href={item.href}
              className={`flex items-center gap-3 px-4 py-3 rounded-lg transition-colors ${
                isActive
                  ? 'bg-red-50 text-red-600 font-medium'
                  : 'text-gray-700 hover:bg-gray-50 hover:text-red-600'
              }`}
            >
              {item.icon}
              <span className="flex-1">{item.name}</span>
              {item.badge && (
                <span className="px-2 py-0.5 text-xs font-semibold bg-gradient-to-r from-purple-500 to-pink-500 text-white rounded-full">
                  {item.badge}
                </span>
              )}
            </Link>
          );
        })}
      </nav>

      {/* Sección de ayuda */}
      <div className="mt-8 p-4 bg-gray-50 rounded-lg">
        <h3 className="text-sm font-semibold text-gray-900 mb-2">¿Necesitas ayuda?</h3>
        <p className="text-xs text-gray-600 mb-3">
          Contacta al equipo de soporte técnico
        </p>
        <button className="w-full bg-red-600 text-white text-sm py-2 rounded-lg hover:bg-red-700 transition-colors">
          Soporte
        </button>
      </div>
    </aside>
  );
}
